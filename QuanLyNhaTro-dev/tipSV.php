				<h3>4. Các nguồn tìm nhà trọ tốt</h3>
				
				Nhờ bạn bè, người thân: Cách này được xem là hiệu quả nhất, hãy đăng lên trang cá nhân trình bày mong muốn của bạn về khu vực cần tìm phòng trọ, hỏi thử xem có ai am hiểu về khu vực đó không? Nếu có người biết, họ sẽ tư vấn khu vực đó có an ninh hay không ? Chủ thế nào ? Giá có đắt hơn so với bên ngoài ? Hoặc có Bạn nào cũng đang có nhu cầu ở ghép thì sẽ giới thiệu cho Bạn.<br><br>

				Đối với các bạn tân sinh viên khi không có người quen thì hãy chủ động vào các group tìm phòng trọ trên Facebook hoặc các trang mua bán nhà đất. Trước hết là ở trường của mình sau đó mới đến các group cộng đồng. Hãy đăng lên các group tìm sự trợ giúp, đang cần tìm phòng ở đâu, giá thế nào… Các anh chị khóa trên rất nhiệt tình sẽ giới thiệu cho bạn. Nếu tin đăng bị trôi thì lâu lâu vào comment vài chấm để bài viết trồi lên lại nhé. Hãy kiên trì vì cách này rất hiệu quả và đáng tin cậy.
				
				<h3>5. Chất lượng phòng trọ</h3>
				
				Không gian nơi ở một phần phụ thuộc vào số người ở và đồ đạc đi kèm, hãy nhắm chừng diện tích phòng để ở thoải mái, không nên chật quá sẽ khiến mọi sinh hoạt bất tiện, hoặc quá rộng sẽ làm lãng phí tiền trọ của bạn. Bạn cần lưu ý về độ nóng của phòng trong mùa khô ở miền Nam, và mùa hè ở miền Bắc nhé. Cũng rất cần được quan tâm đó là khu vực vệ sinh. Nhà vệ sinh phải an toàn, kín đáo và sạch sẽ, hạn chế sử dụng chung với 4 người trở lên vì như thế sẽ vô cùng bất tiện cho sinh hoạt của bạn sau này.
				
				<img src="img/tip3.jpg">
				
				<p>Nên kiểm tra kỹ chất lượng phòng trọ trước khi thuê</p>
				
				Khi thuê phòng bạn sẽ gặp 2 trường hợp: Ở cùng chủ nhà hoặc không.<br><br>

				– Thường người thuê phòng trọ có xu hướng không muốn ở cùng chủ nhà để có thể thoải mái giờ giấc, tự do sinh hoạt. Vậy thì trước khi quyết định thuê phòng trọ riêng biệt, bạn nên kiểm tra kỹ các thiết bị điện, ống nước cũng như các đồ vật có sẵn trong nhà xem có hư hỏng gì không trước khi bạn nhận phòng thuê để yêu cầu chủ nhà sửa lại và để tránh việc bạn chịu những chi phí hao tổn không phải do mình.<br><br>
				
				– Nếu ở cùng chủ, bạn nên chọn chủ nhà có giờ giấc sinh hoạt phù hợp với mình, không quá xét nét đến cuộc sống riêng của bạn. Không dễ để biết chủ nhà có “hợp” với bạn hay không, nhưng hãy để ý qua những lần bạn tiếp xúc, trò chuyện khi đến xem nhà. Bạn có thể hỏi thêm thông tin như: “Tôi có thể dẫn bạn về chơi không?”; “Tôi về khuya quá có ảnh hưởng gì không?”… Và yêu cầu có chìa khóa cổng và phòng riêng.<br><br>

				Ngoài ra, nơi ở cần có đủ chỗ để bạn cất xe bên trong hoặc chỗ giữ xe an toàn gần đó. Bạn cũng cần nhờ chủ nhà hoặc hỏi thông tin về việc đăng ký tạm trú, tạm vắng tại nơi bạn ở nhé.
				
				<h3>6. Kiểm tra những thông tin trong hợp đồng</h3>
				
				Thông thường khi thuê nhà trọ, rất ít người làm hợp đồng. Nhưng làm như vậy sẽ không chắc chắn được quyền lợi cho người thuê, bởi vì chủ nhà có thể đưa ra quyết định “bất chợt” làm cho người thuê không giải quyết kịp. Bạn cũng nên đề cập đến vấn đề đặt cọc tiền thuê phòng trọ trong hợp đồng, trường hợp chuyển nhà trước thời gian quy định thì quy định về số tiền cọc như thế nào? Và thời gian trả tiền phòng là mỗi tháng hay đóng 1 lúc 3 tháng. Tiền phát sinh phí Internet, điện nước,… như thế nào cũng phải ghi cụ thể vào hợp đồng.<br><br>
				
				Số tiền đặt cọc mặc dù ở mỗi nơi mỗi khác, nhưng thông thường số tiền đặt cọc chỉ 1-2 tháng, nếu gặp những trường hợp đặt cọc quá cao các bạn cũng phải lưu ý. Trước khi vào phòng các bạn cũng nên kiểm tra hiện trạng của những cơ sở vật chất bên trong để đến khi chuyển đi không bị ép đền oan. Vào tháng đầu tiên khi chuyển vào các bạn cần kiểm tra số trên đồng hồ điện và nước, để tránh trường hợp trả tiền điện nước cho người xài trước đó.
				
				<h3>7. Điện, nước của phòng trọ</h3>
				
				Khi thuê phòng bạn nên hỏi chủ nhà xem giá tiền điện, nước, phí ở đó như thế nào. Nếu sử dụng nước theo khối yêu cầu cần có đồng hồ để tính. Tùy vào từng khu vực bạn ở mà giá tiền điện, nước sẽ được chủ nhà tính với giá khác nhau.
