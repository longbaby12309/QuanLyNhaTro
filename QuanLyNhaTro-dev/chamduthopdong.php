				<h2>CHẤM DỨT HỢP ĐỒNG THUÊ NHÀ CÓ DỄ KHÔNG?</h2>
				
				Hiện nay, nhu cầu thuê nhà luôn ở mức cao vì sinh viên, người lao động tại các tỉnh tập trung về thành phố lớn với số lượng ngày càng tăng. Và điều làm người thuê nhà luôn trăn trở, đó chính là nội dung hợp đồng thuê nhà, trong đó có vấn đề về <b>chấm dứt hợp đồng thuê nhà.</b> Vậy vấn đề này được pháp luật quy định như thế nào? Bên cho thuê và bên thuê cần lưu ý nội dung gì khi chấm dứt hợp đồng? Hãy cùng NPLaw tìm hiểu qua bài viết sau đây.<br><br>
				
				<img src="img/cdhp1.jpg">
				
				<h3>I. Thực trạng về chấm dứt hợp đồng thuê nhà</h3>
				
				Khi thỏa thuận các nội dung của hợp đồng thuê nhà, ngoài các vấn đề cơ bản như thời hạn thuê, giá thuê, hình thức thanh toán, tiền thuê,... thì một nội dung cũng rất quan trọng khác mà cả bên cho thuê và bên thuê cần lưu ý là điều khoản quy định về <b>chấm dứt hợp đồng thuê nhà.</b><br><br>

				Hiện nay, có rất nhiều người khi thuê nhà bị chủ nhà đơn phương chấm dứt hợp đồng thuê dẫn đến không có chỗ ở, nhưng không hiểu rõ quy định pháp luật để bảo vệ quyền lợi của bản thân. Ngược lại, bên cho thuê cũng không biết cách đơn phương chấm dứt hợp đồng khi người thuê nhà có một số hành vi là căn cứ cho việc kết thúc hợp đồng thuê nhà.
				
				<h3>II. Quy định pháp luật về chấm dứt hợp đồng thuê nhà</h3>
				
				Các vấn đề về <b>chấm dứt hợp đồng thuê nhà</b> được <b>Luật Nhà ở 2014</b> điều chỉnh bao gồm một số quy định như sau:
				
				<h4>1. Các trường hợp chấm dứt hợp đồng thuê nhà</h4>
				
				<b>Thứ nhất</b>, trường hợp thuê nhà ở thuộc sở hữu nhà nước thì việc <b>chấm dứt hợp đồng thuê nhà</b> được thực hiện khi có một trong các trường hợp như: Bán, cho thuê, cho thuê mua nhà ở không đúng thẩm quyền, không đúng đối tượng hoặc không đủ điều kiện theo quy định của Luật Nhà ở 2014; Hết thời hạn thuê theo hợp đồng mà bên thuê không còn nhu cầu thuê tiếp hoặc khi hai bên thỏa thuận chấm dứt hợp đồng thuê, thuê mua nhà ở; Bên thuê, trả lại nhà ở đang thuê,...<br><br>

				<b>Thứ hai</b>, trường hợp thuê nhà ở không thuộc sở hữu nhà nước thì việc chấm dứt hợp đồng thuê nhà ở được thực hiện khi có một trong các trường hợp sau đây:
				
				<ul>
					<li>Hợp đồng thuê nhà ở hết hạn; trường hợp trong hợp đồng không xác định thời hạn thì hợp đồng chấm dứt sau 90 ngày, kể từ ngày bên cho thuê nhà ở thông báo cho bên thuê nhà ở biết việc chấm dứt hợp đồng.</li>

					<li>Hai bên thỏa thuận chấm dứt hợp đồng.</li>

					<li>Nhà ở cho thuê không còn.</li>

					<li>Bên thuê nhà ở chết hoặc có tuyên bố mất tích của Tòa án mà khi chết, mất tích không có ai đang cùng chung sống.</li>

					<li>Nhà ở cho thuê bị hư hỏng nặng, có nguy cơ sập đổ hoặc thuộc khu vực đã có quyết định thu hồi đất, giải tỏa nhà ở hoặc có quyết định phá dỡ của cơ quan nhà nước có thẩm quyền; nhà ở cho thuê thuộc diện bị Nhà nước trưng mua, trưng dụng để sử dụng vào các mục đích khác. Đối với trường hợp này, bên cho thuê phải thông báo bằng văn bản cho bên thuê biết trước 30 ngày về việc chấm dứt hợp đồng thuê nhà ở quy định tại điểm này, trừ trường hợp các bên có thỏa thuận khác.</li>

					<li>Các trường hợp đơn phương chấm dứt thực hiện hợp đồng thuê nhà ở.</li>
				</ul>
				
				<h4>2. Quy định về chấm dứt hợp đồng thuê nhà</h4>
				
				Các quy định về <b>chấm dứt hợp đồng thuê nhà</b> được quy định bởi Luật Nhà ở 2014. Các trường hợp chấm dứt hợp đồng được chia thành hai nhóm là đương nhiên chấm dứt hợp đồng và đơn phương chấm dứt hợp đồng. Đối với quy định về đơn phương chấm dứt hợp đồng sẽ chia thành hai trường hợp tương ứng với chủ thể đề nghị chấm dứt là bên cho thuê hay bên thuê.
				
				<h4>3. Đơn phương chấm dứt hợp đồng thuê nhà được quy định như thế nào?</h4>
				
				Nguyên tắc là trong quá trình thực hiện hợp đồng thuê nhà, bên cho thuê không được đơn phương chấm dứt hợp đồng và thu hồi nhà ở đang cho thuê. Tuy nhiên, trong một số trường hợp quy định tại Điều 132 <b>Luật Nhà ở 2014</b> cho phép cả bên cho thuê và bên thuê đều được quyền đơn phương <b>chấm dứt hợp đồng thuê nhà</b>, cụ thể như sau:<br><br>

				<b>Thứ nhất</b>, bên cho thuê nhà ở có quyền đơn phương chấm dứt thực hiện hợp đồng thuê nhà và thu hồi nhà ở đang cho thuê khi thuộc một trong các trường hợp sau đây:
				
				<ul>
					<li>Bên cho thuê nhà ở thuộc sở hữu nhà nước, nhà ở xã hội cho thuê không đúng thẩm quyền, không đúng đối tượng, không đúng điều kiện theo quy định của Luật Nhà ở 2014.</li>

					<li>Bên thuê không trả tiền thuê nhà ở theo thỏa thuận từ 03 tháng trở lên mà không có lý do chính đáng.</li>

					<li>Bên thuê sử dụng nhà ở không đúng mục đích như đã thỏa thuận trong hợp đồng.</li>

					<li>Bên thuê tự ý đục phá, cơi nới, cải tạo, phá dỡ nhà ở đang thuê.</li>

					<li>Bên thuê chuyển đổi, cho mượn, cho thuê lại nhà ở đang thuê mà không có sự đồng ý của bên cho thuê.</li>

					<li>Bên thuê làm mất trật tự, vệ sinh môi trường, ảnh hưởng nghiêm trọng đến sinh hoạt của những người xung quanh đã được bên cho thuê nhà ở hoặc tổ trưởng tổ dân phố, trưởng thôn, làng, ấp, bản, buôn, phum, sóc lập biên bản đến lần thứ ba mà vẫn không khắc phục.</li>

					<li>Bên cho thuê và bên thuê không thỏa thuận được giá thuê mới trong trường hợp bên cho thuê cải tạo làm tăng giá trị nhà ở.</li>
				</ul>
				
				<b>Thứ hai</b>, bên thuê nhà ở có quyền đơn phương chấm dứt thực hiện hợp đồng thuê nhà ở khi bên cho thuê nhà ở có một trong các hành vi sau đây:
				
				<ul>
					<li>Không sửa chữa nhà ở khi có hư hỏng nặng.</li>

					<li>Tăng giá thuê nhà ở bất hợp lý hoặc tăng giá thuê mà không thông báo cho bên thuê nhà ở biết trước theo thỏa thuận.</li>

					<li>Khi quyền sử dụng nhà ở bị hạn chế do lợi ích của người thứ ba.</li>
				</ul>
				
				<h4>4. Chấm dứt hợp đồng thuê nhà sai quy định bị xử lý như thế nào?</h4>
				
				<b>Chấm dứt hợp đồng thuê</b> nhà sai quy định của Luật Nhà ở 2014 sẽ phải chịu trách nhiệm bồi thường thiệt hại (nếu có) cho bên bị chấm dứt hợp đồng. Ngoài ra, tương tự như các loại hợp đồng khác, khi giao kết, các bên có thể thỏa thuận điều khoản phạt vi phạm đối với trường hợp chấm dứt hợp đồng không đúng quy định pháp luật.
				<img src="img/cdhp2.jpg">
				
				<h3>III. Giải đáp một số câu hỏi liên quan đến chấm dứt hợp đồng thuê nhà</h3>
				
				Xoay quanh vấn đề <b>chấm dứt hợp đồng thuê nhà</b> có một số câu hỏi thường gặp được FPOLY House giải đáp như sau:

				<h4>1. Đơn phương chấm dứt hợp đồng thuê nhà có lấy lại được tiền đặt cọc không?</h4>
				
				Theo quy định của <b>Bộ luật Dân sự 2015</b>, đặt cọc là việc bên đặt cọc giao cho bên nhận cọc một khoản tiền hoặc kim khí quý, đá quý hoặc vật có giá trị khác (gọi chung là tài sản đặt cọc) trong một khoảng thời gian để đảm bảo giao kết hoặc thực hiện hợp đồng.<br><br>

				Nếu việc đơn phương <b>chấm dứt hợp đồng thuê nhà</b> được thực hiện đúng quy định pháp luật và theo thỏa thuận trong hợp đồng khi thuê nhà thì lúc này mới được hoàn lại tài sản cọc.
				
				<h4>2. Đơn phương chấm dứt hợp đồng thuê nhà có phải báo trước cho bên còn lại không?</h4>
				
				Câu trả lời là <b>có</b>. Bên đơn phương chấm dứt thực hiện hợp đồng thuê nhà ở phải thông báo cho bên kia biết trước <b>ít nhất 30 ngày</b>, trừ trường hợp các bên có thỏa thuận khác. nếu vi phạm quy định tại khoản này mà gây thiệt hại thì phải bồi thường theo quy định của pháp luật.

				<h4>3. Chấm dứt hợp đồng thuê nhà cần thực hiện những thủ tục gì không?</h4>
				
				Khi chấm dứt hợp đồng, các bên cần xác định trường hợp chấm dứt là đương nhiên hay đơn phương. Đối với đơn phương chấm dứt hợp đồng, các bên nên thực hiện theo các bước như sau:<br><br>
				
				<b>- Bước 1:</b> Bên cho thuê hoặc bên thuê xác định căn cứ đơn phương chấm dứt hợp đồng thuê;<br><br>

				<b>- Bước 2:</b> Bên đơn phương chấm dứt hợp đồng phải báo cho bên còn lại biết trước ít nhất 30 ngày, trừ trường hợp các bên có thỏa thuận khác.<br><br>

				<b>- Bước 3:</b> Nếu các bên đạt được thỏa thuận thì lập biên bản thanh lý hợp đồng thuê nhà.<br><br>
				
